#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;

	dest = malloc(sizeof(char) *(ft_strlen(s) + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (s[i++])
		dest[i] = s[i];
	dest[i] = '\0';
	return (dest);
}
